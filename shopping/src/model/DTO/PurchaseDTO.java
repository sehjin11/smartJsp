package model.DTO;

public class PurchaseDTO {
	String purchaseNum;
	String memId;
	String purchaseTotPrice;
	String purchaseAddr;
	String purchaseMethod;
	String purchseRequest;
	String purchseDate;
	String receiverName;
	String receiverPhone;
	
	public String getPurchaseNum() {
		return purchaseNum;
	}
	public void setPurchaseNum(String purchaseNum) {
		this.purchaseNum = purchaseNum;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getPurchaseTotPrice() {
		return purchaseTotPrice;
	}
	public void setPurchaseTotPrice(String purchaseTotPrice) {
		this.purchaseTotPrice = purchaseTotPrice;
	}
	public String getPurchaseAddr() {
		return purchaseAddr;
	}
	public void setPurchaseAddr(String purchaseAddr) {
		this.purchaseAddr = purchaseAddr;
	}
	public String getPurchaseMethod() {
		return purchaseMethod;
	}
	public void setPurchaseMethod(String purchaseMethod) {
		this.purchaseMethod = purchaseMethod;
	}
	public String getPurchseRequest() {
		return purchseRequest;
	}
	public void setPurchseRequest(String purchseRequest) {
		this.purchseRequest = purchseRequest;
	}
	public String getPurchseDate() {
		return purchseDate;
	}
	public void setPurchseDate(String purchseDate) {
		this.purchseDate = purchseDate;
	}
	public String getReceiverName() {
		return receiverName;
	}
	public void setReceiverName(String receiverName) {
		this.receiverName = receiverName;
	}
	public String getReceiverPhone() {
		return receiverPhone;
	}
	public void setReceiverPhone(String receiverPhone) {
		this.receiverPhone = receiverPhone;
	}
	
	
}
